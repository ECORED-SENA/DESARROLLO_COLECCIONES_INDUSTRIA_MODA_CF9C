    h4 Sistema perforado
    p.mb-5 Se efectúa el trazo (marcado) sobre papel; una vez dibujado, se procede a perforar el contorno de cada una de las piezas o figuras con un pin o punzón. Para reproducir esta marca sobre el tejido, basta colocarlo sobre el mismo y espolvorearlo con polvos tipo negro humo o talco, pasando estos tejidos y quedando adheridos a él; la marcada tiene una larga duración. Este se realiza también para marcar zonas específicas en confección, como dibujo de bolsillos, curvas y aquellas operaciones complejas que sin una guía no se lograrían. (Sena, 2012)


    h4 Sistema por pulverización
    p.mb-5 Los patrones deben estar completos (ejemplo: los patrones marcados 2 x talla y marcados izquierdo y derecho). Se colocan estos sobre el tejido o el papel como armando un rompecabezas. Terminado el trazo, se procede a espolvorear (polvos tipo negro humo o talco) en los espacios vacíos, quedando adheridos al material   y marcada la silueta de los moldes. Se completa la marcada colocando un adhesivo (cinta pegante) en el cual se coloca la información deseada (talla, lote, etc.)

    h4 Sistema fotográfico
    p.mb-5 Se extiende el papel fotográfico sensibilizado sobre la mesa, se depositan los patrones sobre él, siguiendo la posición del estudio previo. Se pasa, seguidamente, un carro que transporta potentes focos de luz ultravioleta que actúan sobre las partes del papel que está al descubierto, luego, se fija esta imagen escalando el papel en atmósfera amoniacal. El perfil de los patrones queda marcado con extrema nitidez. (Sena, 2012).

    

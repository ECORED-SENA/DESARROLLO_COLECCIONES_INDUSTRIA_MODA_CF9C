      .h2 4.4 Trazo en telas con rayas, cuadros, estampados y tejido con pelo


    p.mb-5 Una de las cosas a tener en cuenta a la hora de comprar tela con rayas y/o cuadros es la regularidad o irregularidad de su diseño. Después de identificar la regularidad de la tela, existen diferentes precauciones que se deben tener a la hora de hacer los trazos:

